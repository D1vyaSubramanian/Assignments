package week1.day2assignments;

public class CheckIfTheNumberIsPositiveOrNegative {
public static void main(String[] args) {
	int num=50;
	if(num>0) {
		System.out.println("\n The number is positive \t"+num);
	}else if(num<0) {
		System.out.println("\n The number is negative \t"+num);
	}else {
		System.out.println("\n The number is neither positive nor negative \t"+num);
	}
		
		
}


}